package problems.daily_problems.problem_9;

import java.util.Scanner;

public class problem9 {


    /*Given a list of integers, write a function that returns the largest sum
    of non-adjacent numbers. Numbers can be 0 or negative.

    For example, [2, 4, 6, 2, 5] should return 13, since we
    pick 2, 6, and 5. [5, 1, 1, 5] should return 10, since we pick 5 and 5.*/

    public static void main(String[] args) {
        Scanner ip = new Scanner(System.in);
        System.out.println("Enter length of array");

        int n = ip.nextInt();

        System.out.println("Enter the numbers");

        int [] arr = new int[n];
        for(int i =0; i< n ; i++){
            arr[i] = ip.nextInt();
        }

        int include = arr[0];
        int exclude = 0;

        for(int i =0; i< arr.length; i++){
            int next_include = exclude + arr[i];
            int next_exclude = Math.max(include, exclude);

            include = next_include;
            exclude = next_exclude;
        }

        int ans = Math.max(include,exclude);

        System.out.println(ans);
    }
}





































































































































































































































































































































































